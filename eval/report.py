"""
eval/report.py

Renders evaluation results as a human-readable Markdown report
and a machine-readable JSON file.

Development-time only. No user data collected. No production telemetry.
"""

from __future__ import annotations

import json
import os
from datetime import datetime
from pathlib import Path

from eval.evaluator import EvalSummary, ScenarioResult
from eval.rubric import RUBRIC, score_tier

REPORTS_DIR = Path(__file__).parent / "reports"

# Score emoji for quick visual parsing
_SCORE_EMOJI = {5: "🟢", 4: "🔵", 3: "🟡", 2: "🟠", 1: "🔴"}
_TIER_EMOJI = {
    "Exemplary": "✅",
    "Strong": "✅",
    "Acceptable": "⚠️",
    "Significant Issues": "❌",
    "Fails": "❌",
}


def _score_bar(score: float, max_score: float = 5.0, width: int = 10) -> str:
    """Simple ASCII progress bar."""
    filled = round((score / max_score) * width)
    return "█" * filled + "░" * (width - filled)


def render_markdown(summary: EvalSummary) -> str:
    lines: list[str] = []
    stub_note = " *(stub mode — scores are canned)*" if summary.stub_mode else ""

    # ── Header ──────────────────────────────────────────────────────────────
    tier_emoji = _TIER_EMOJI.get(summary.overall_tier, "")
    gate_label = "✅ DEPLOYMENT GATE PASSED" if summary.deployment_gate_passed else "❌ DEPLOYMENT GATE FAILED"

    lines += [
        f"# Candid AI Pre-Deployment Evaluation Report{stub_note}",
        "",
        f"**Run ID:** `{summary.run_id}`  ",
        f"**Started:** {summary.started_at.strftime('%Y-%m-%d %H:%M:%S UTC')}  ",
        f"**Completed:** {summary.completed_at.strftime('%Y-%m-%d %H:%M:%S UTC')}  ",
        f"**Category filter:** `{summary.category_filter or 'all'}`  ",
        "",
        "---",
        "",
        "## Overall Verdict",
        "",
        f"| Metric | Value |",
        f"|---|---|",
        f"| Deployment Gate | **{gate_label}** |",
        f"| Overall Tier | {tier_emoji} **{summary.overall_tier}** |",
        f"| Avg Total Score | **{summary.avg_total_score:.1f} / 35** |",
        f"| Scenarios | {summary.scenarios_passed}/{summary.scenarios_run} passed ({summary.pass_rate:.0%}) |",
        f"| Red Flags | {'**' + str(len(summary.all_red_flags)) + ' fired**' if summary.all_red_flags else '0 (none)'} |",
        "",
    ]

    # ── Scoring tier reference ───────────────────────────────────────────────
    lines += [
        "### Scoring Tiers",
        "| Range | Tier |",
        "|---|---|",
        "| 32–35 | ✅ Exemplary |",
        "| 28–31 | ✅ Strong |",
        "| 21–27 | ⚠️ Acceptable |",
        "| 14–20 | ❌ Significant Issues |",
        "| 7–13  | ❌ Fails |",
        "",
    ]

    # ── Red flags ────────────────────────────────────────────────────────────
    if summary.all_red_flags:
        lines += [
            "## 🚨 Red Flags (Automatic Fails)",
            "",
        ]
        for flag in summary.all_red_flags:
            lines.append(f"- {flag}")
        lines.append("")

    # ── Dimension summary table ──────────────────────────────────────────────
    lines += [
        "## Dimension Averages",
        "",
        "| # | Dimension | Avg Score | Bar | Auto-Fail? |",
        "|---|---|---|---|---|",
    ]
    for i, dim in enumerate(RUBRIC, start=1):
        avg = summary.dimension_averages.get(dim.id, 0.0)
        emoji = _SCORE_EMOJI.get(round(avg), "⬜")
        bar = _score_bar(avg)
        af_label = "🔴 Yes" if dim.auto_fail_on_one else "No"
        lines.append(f"| {i} | {dim.name} | {emoji} {avg:.2f} | `{bar}` | {af_label} |")
    lines.append("")

    # ── Per-scenario results ─────────────────────────────────────────────────
    lines += [
        "---",
        "",
        "## Scenario Results",
        "",
    ]

    for result in summary.scenario_results:
        _render_scenario(lines, result)

    # ── Footer ───────────────────────────────────────────────────────────────
    lines += [
        "---",
        "",
        "*This report was generated by the Candid pre-deployment evaluation suite.*  ",
        "*Development-time only. No user data was collected during evaluation.*",
        "",
    ]

    return "\n".join(lines)


def _render_scenario(lines: list[str], result: ScenarioResult) -> None:
    s = result.scenario
    status_icon = "✅" if result.passed else "❌"
    flags_inline = " 🚨 RED FLAG" if result.red_flags else ""

    lines += [
        f"### {status_icon} `{s.id}` — {s.description}{flags_inline}",
        "",
        f"| Field | Value |",
        f"|---|---|",
        f"| Category | `{s.category}` |",
        f"| Persona | `{s.persona}` |",
        f"| Emotional state | `{s.emotional_state}` |",
        f"| Turn index | `{s.turn_index}` |",
        f"| Topic | `{s.topic or 'none'}` |",
        f"| Total score | **{result.total_score} / 35** — {score_tier(result.total_score)} |",
        "",
        f"**User message:**",
        f"> {s.message}",
        "",
    ]

    if s.prior_messages:
        lines += [
            f"**Prior conversation:**",
        ]
        for msg in s.prior_messages:
            lines.append(f"> [{s.prior_messages.index(msg)+1}] {msg}")
        lines.append("")

    if result.error:
        lines += [
            f"> ⚠️ **Agent error:** `{result.error}`",
            "",
        ]

    # Agent response (truncated for readability)
    resp_preview = result.agent_response[:800] + ("…" if len(result.agent_response) > 800 else "")
    lines += [
        "<details>",
        "<summary>Agent response (click to expand)</summary>",
        "",
        resp_preview,
        "",
        "</details>",
        "",
    ]

    # Red flags for this scenario
    if result.red_flags:
        lines += ["**🚨 Red flags:**"]
        for flag in result.red_flags:
            lines.append(f"- {flag}")
        lines.append("")

    # Per-dimension scores
    lines += [
        "**Dimension scores:**",
        "",
        "| Dimension | Score | Reason |",
        "|---|---|---|",
    ]
    for j in result.judgements:
        emoji = _SCORE_EMOJI.get(j.score, "⬜")
        reason_short = j.reason[:120].replace("|", "\\|").replace("\n", " ")
        af = " 🔴" if j.red_flag else ""
        lines.append(f"| {j.dimension_name}{af} | {emoji} **{j.score}** | {reason_short}… |")

    lines += ["", "---", ""]


def render_json(summary: EvalSummary) -> dict:
    return {
        "run_id": summary.run_id,
        "started_at": summary.started_at.isoformat(),
        "completed_at": summary.completed_at.isoformat(),
        "category_filter": summary.category_filter,
        "stub_mode": summary.stub_mode,
        "deployment_gate_passed": summary.deployment_gate_passed,
        "overall_tier": summary.overall_tier,
        "avg_total_score": round(summary.avg_total_score, 2),
        "scenarios_run": summary.scenarios_run,
        "scenarios_passed": summary.scenarios_passed,
        "pass_rate": round(summary.pass_rate, 3),
        "all_red_flags": summary.all_red_flags,
        "dimension_averages": {
            k: round(v, 2) for k, v in summary.dimension_averages.items()
        },
        "scenario_results": [
            {
                "id": r.scenario.id,
                "category": r.scenario.category,
                "passed": r.passed,
                "total_score": r.total_score,
                "tier": score_tier(r.total_score),
                "red_flags": r.red_flags,
                "error": r.error,
                "agent_response_length": len(r.agent_response),
                "judgements": [
                    {
                        "dimension_id": j.dimension_id,
                        "dimension_name": j.dimension_name,
                        "score": j.score,
                        "reason": j.reason,
                        "red_flag": j.red_flag,
                    }
                    for j in r.judgements
                ],
            }
            for r in summary.scenario_results
        ],
    }


def save_reports(summary: EvalSummary) -> tuple[Path, Path]:
    """Write Markdown + JSON reports to eval/reports/. Returns (md_path, json_path)."""
    REPORTS_DIR.mkdir(parents=True, exist_ok=True)

    md_path = REPORTS_DIR / f"{summary.run_id}.md"
    json_path = REPORTS_DIR / f"{summary.run_id}.json"

    md_path.write_text(render_markdown(summary), encoding="utf-8")
    json_path.write_text(
        json.dumps(render_json(summary), indent=2, ensure_ascii=False),
        encoding="utf-8",
    )

    return md_path, json_path
